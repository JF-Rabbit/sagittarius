package org.sagittarius.uitest.exception;

public class SwipeLocationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SwipeLocationException() {
		super();
	}

	public SwipeLocationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SwipeLocationException(String message, Throwable cause) {
		super(message, cause);
	}

	public SwipeLocationException(String message) {
		super(message);
	}

	public SwipeLocationException(Throwable cause) {
		super(cause);
	}

}
